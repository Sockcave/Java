/* Casting: assigning a value of one type to a variable of ANOTHER TYPE.
*/

public class casting {

    public static void main(String[] args) {

        // Example
        int a = (int) 3.14;
        System.out.println(a);

        //Example 2
        double b = 42.571;
        int c = (int) b;
        System.out.println(c);

    }
    
}
